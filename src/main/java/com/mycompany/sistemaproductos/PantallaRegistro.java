    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
        // TODO add your handling code here agregar:

        String nombre = tfNombre.getText().trim();
        String precioStr = tfPrecio.getText().trim();
        String stockStr = tfStock.getText().trim();

        if (nombre.isEmpty() || precioStr.isEmpty() || stockStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos deben estar completos.",
                    "Campos vacíos", JOptionPane.WARNING_MESSAGE);
            return;
        }

        double precio;
        int stock;
        try {
            precio = Double.parseDouble(precioStr);
            stock = Integer.parseInt(stockStr);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Precio y Stock deben ser numéricos");
            return;
        }
        
        //Agregar la funcionalidad de espeocofocar la unidad
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost/sistemaproductos", "root", "")) {

            DefaultTableModel m = (DefaultTableModel) TableDatos.getModel();

            if (idSeleccionado == -1) {              // ACA SE INSERTAR EL PRODUCTO
                String sql = "INSERT INTO productos (nombre, precio, stock) VALUES (?, ?, ?)";
                try (PreparedStatement ps = con.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS)) {
                    ps.setString(1, nombre);
                    ps.setDouble(2, precio);
                    ps.setInt(3, stock);

                    if (ps.executeUpdate() > 0) {
                        try (ResultSet rs = ps.getGeneratedKeys()) {
                            if (rs.next()) {
                                int id = rs.getInt(1);
                                m.insertRow(0, new Object[]{id, nombre, precio, stock});
                                JOptionPane.showMessageDialog(this, "Producto agregado");
                            }
                        }
                    }
                }
            } else {                                 // ACA SE ACTUALIZA EL PRODUCTO
                String sql = "UPDATE productos SET nombre=?, precio=?, stock=? WHERE id=?";
                try (PreparedStatement ps = con.prepareStatement(sql)) {
                    ps.setString(1, nombre);
                    ps.setDouble(2, precio);
                    ps.setInt(3, stock);
                    ps.setInt(4, idSeleccionado);

                    if (ps.executeUpdate() > 0) {
                        // refrescamos la fila en la tabla
                        m.setValueAt(nombre, filaSeleccionada, 1);
                        m.setValueAt(precio, filaSeleccionada, 2);
                        m.setValueAt(stock, filaSeleccionada, 3);
                        JOptionPane.showMessageDialog(this, "Producto actualizado");
                    }
                }
            }

            limpiarCampos();   // siempre al final
        } catch (SQLException ex) {
            Logger.getLogger(PantallaRegistro.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error de BD: " + ex.getMessage(),
                    "Base de datos", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BtnAgregarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        // TODO add your handling code here funcion eliminar :
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecciona un producto de la tabla para eliminar.");
            return;
        }
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Estás seguro de eliminar este producto?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
        if (confirmacion != JOptionPane.YES_OPTION) {
            return;
        }
        int id = (int) TableDatos.getValueAt(filaSeleccionada, 0);

        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost/sistemaproductos", "root", "")) {
            String sql = "DELETE FROM productos WHERE id = ?";

            try (PreparedStatement ps = con.prepareStatement(sql)) {
                ps.setInt(1, id);
                int filas = ps.executeUpdate();

                if (filas > 0) {
                    DefaultTableModel model = (DefaultTableModel) TableDatos.getModel();
                    model.removeRow(filaSeleccionada);

                    JOptionPane.showMessageDialog(null, "Producto eliminado correctamente.");
                    limpiarCampos();
                } else {

                    JOptionPane.showMessageDialog(null, "No se pudo eliminar correctamente.");

                }

            }

        } catch (SQLException ex) {
            Logger.getLogger(PantallaRegistro.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "No se pudo conectar a la base de datos");
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

