    //Funcion para poder buscar productos en la base de datos 
    private void buscarProducto() {
        String texto = txtBuscar.getText().trim();//y obtiene el texto ingresado y elimina los espacio en blanco del inicio y del final de la cadena 
        DefaultTableModel model = (DefaultTableModel) TableDatos.getModel();//Obtiene el modelo de la tabla para poder modificar el contenido
        model.setRowCount(0);//Limpia completamente la tabla 

        //Se conecta a la base de datos 
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost/sistemaproductos", "root", "")) {

            String sql = "SELECT * FROM productos WHERE nombre LIKE ? OR precio LIKE ?";//Preparo la consulta SQL
            PreparedStatement ps = con.prepareStatement(sql);//
            
            ps.setString(1, "%" + texto + "%");
            ps.setString(2, "%" + texto + "%");
            
            ResultSet rs = ps.executeQuery();//Ejecuto la consulta y encapsulo los datos en un obejto ResulSet
            
            while (rs.next()) {//Lee las filas de los datos en la base de datos y los muestras en el modelo de tabla de java 
                model.addRow(new Object[]{// Añado los datos de la base de datos al modelo de tabla de Java 
                    rs.getInt("id"),
                    rs.getString("nombre"),
                    rs.getDouble("precio"),
                    rs.getInt("stock")
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al buscar productos");
        }

    }

